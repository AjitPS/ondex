			<div class="instructions">
				<p>
					OndexWeb adopts some well accepted conventions to represent members
					of the network. <br /> <strong>Nodes</strong> represent <strong>Concepts</strong>
					and can be Genes, Biological process, ... or Publications and
					Phenotypes. <br /> <strong>Edges</strong> (the links between nodes)
					represent <strong>relations</strong> between two concepts; these
					can be <strong>encodes</strong>, has_smilar_sequences.
					participates_in, interacts_with, published_in.
				</p>
