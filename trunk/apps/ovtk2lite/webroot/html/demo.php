<div ID="usage">


<pre>
CTRL+c = center graph to fit into display
CTRL+m = special function for QTLNetMiner, load appearance and size
CTRL+z = undo last visibility change
CTRL+y = redo last visibility change
CTRL+a = select all nodes
CTRL+h = hide selection
CTRL+g = hide everything but selection
Numpad - = zoom out
Numpad + = zoom in
</pre>
<p>
Mouse behaviour similar to Ondex Desktop Application</p>
<ul>
<li> left click on empty space and holding moves graph </li>
<li> mouse wheel for zooming in / out</li>
<li> left click on nodes or edges selects them</li>
<li> right click shows context menu</li>
</ul>
